import math

# Basic mathematical constants
print("Pi:", math.pi)
print("Euler's number (e):", math.e)

# Trigonometric functions
angle = math.radians(30)  # Convert degrees to radians
print("sin(30°):", math.sin(angle))
print("cos(30°):", math.cos(angle))
print("tan(30°):", math.tan(angle))

# Logarithmic functions
print("log(10):", math.log(10))  # Natural log (base e)
print("log10(100):", math.log10(100))  # Log base 10

# Exponential and power functions
print("e^2:", math.exp(2))
print("2^3:", math.pow(2, 3))

# Rounding functions
print("Floor of 4.7:", math.floor(4.7))
print("Ceil of 4.3:", math.ceil(4.3))

# Factorial
print("Factorial of 5:", math.factorial(5))

# Square root
print("Square root of 25:", math.sqrt(25))

# GCD (Greatest Common Divisor)
print("GCD of 48 and 18:", math.gcd(48, 18))

# Absolute value
print("Absolute value of -7.5:", math.fabs(-7.5))
